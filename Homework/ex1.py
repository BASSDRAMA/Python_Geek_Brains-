var1 = int(input('Введите целое число'))
var2 = int(input('Введите еще одно целое число'))
print('Сумма чисел равна', var1 + var2)
print('Разница чисел равна', var1 - var2)
print('Умножение чисел равно', var1 * var2)
print('Деление чисел равно', var1 / var2)
print('Целое деление чисел равно', var1 // var2)